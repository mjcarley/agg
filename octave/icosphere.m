function [x, t, u, v]=icosphere()

## http://blog.andreaskahler.com/2009/06/creating-icosphere-mesh-in-code.html
  
  t = [0   11   5;
       0    5   1;
       0    1   7;
       0    7  10;
       0   10  11;
       1    5   9;
       5   11   4;
       11  10   2;
       10   7   6;
       7    1   8;
       3    9   4;
       3    4   2;
       3    2   6;
       3    6   8;
       3    8   9;
       4    9   5;
       2    4  11;
       6    2  10;
       8    6   7;
       9    8   1] ;
  
  rt = 0.5*(1.0 + sqrt(5.0)) ;

  x( 1, 1) = -1.0 ; x( 1, 2) =  rt  ; x( 1, 3) =  0.0 ; 
  x( 2, 1) =  1.0 ; x( 2, 2) =  rt  ; x( 2, 3) =  0.0 ; 
  x( 3, 1) = -1.0 ; x( 3, 2) = -rt  ; x( 3, 3) =  0.0 ; 
  x( 4, 1) =  1.0 ; x( 4, 2) = -rt  ; x( 4, 3) =  0.0 ; 
  		        		  
  x( 5, 1) =  0.0 ; x( 5, 2) = -1.0 ; x( 5, 3) =   rt ; 
  x( 6, 1) =  0.0 ; x( 6, 2) =  1.0 ; x( 6, 3) =   rt ; 
  x( 7, 1) =  0.0 ; x( 7, 2) = -1.0 ; x( 7, 3) =  -rt ; 
  x( 8, 1) =  0.0 ; x( 8, 2) =  1.0 ; x( 8, 3) =  -rt ; 
		        		  
  x( 9, 1) =  rt  ; x( 9, 2) =  0.0 ; x( 9, 3) =  -1.0 ; 
  x(10, 1) =  rt  ; x(10, 2) =  0.0 ; x(10, 3) =   1.0 ; 
  x(11, 1) = -rt  ; x(11, 2) =  0.0 ; x(11, 3) =  -1.0 ; 
  x(12, 1) = -rt  ; x(12, 2) =  0.0 ; x(12, 3) =   1.0 ; 

  R = sqrt(1+rt.^2) ;
  x /= R ;

  x(:,3) = (x(:,3)+1)/2 ;
  x(:,1:2) /= 2 ;

  u = x(:,3) ;

  v = (x(:,1)./sqrt(u.*(1-u))+1)/2 ;
  ii = find(x(:,2)<0) ;
  v(ii) = -v(ii) ; 
